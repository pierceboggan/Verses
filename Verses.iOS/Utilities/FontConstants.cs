using MonoTouch.UIKit;

namespace Verses.iOS
{
	public class FontConstants
	{
		public static UIFont SourceSansProRegular (float size)
		{
			return UIFont.FromName ("SourceSansPro-Regular", size);
		}

		public static UIFont SourceSansProBold (float size)
		{
			return UIFont.FromName ("SourceSansPro-Bold", size);
		}
	}
}